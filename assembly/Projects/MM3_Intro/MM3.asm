SOUND_PAINT		PROC		NEAR
			SOUND_ON (FRQN)
			PAINT_SINGLE (0FFH),(LAZR)
			INC			LAZR
			MOV			TIMR,0
		LPP1:
			MOV 		CX,65535
		LPP2:
			LOOP		LPP2
			INC			TIMR
			DEC			FINL	; Time (DX). 24 = 1 sec
			MOV			AX,TIMR
			DIV			DVDR
			MOV			AH,0
			CMP			AX,2
			JE			LPP3
		LPP4:
			CMP			FINL,0
			JNZ			LPP1
			DEC			LAZR
			PAINT_SINGLE (CLRS),(LAZR)	; Paint
			RET
		LPP3:
			PAINT_SINGLE (0FFH),(LAZR)	; Laser
			DEC			LAZR
			PAINT_SINGLE (CLRS),(LAZR)	; Paint
			ADD			LAZR,02
			MOV			TIMR,0
			JMP			LPP4
			RET
			ENDP
